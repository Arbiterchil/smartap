package com.example.arbiterchil.smartap;

public class notif {

    String name,note;

    public notif(){

    }

    public notif(String name, String note) {
        this.name = name;
        this.note = note;
    }

    public String getName() {
        return name;
    }

    public String getNote() {
        return note;
    }
}
